import { createStorefrontApiClient } from '@shopify/storefront-api-client';

const storeDomain = import.meta.env.VITE_SHOPIFY_STORE_DOMAIN || '';
const apiToken = import.meta.env.VITE_SHOPIFY_STOREFRONT_API_TOKEN || '';
const apiVersion = import.meta.env.VITE_SHOPIFY_API_VERSION || '2024-01';

export const shopifyClient = createStorefrontApiClient({
  storeDomain,
  apiVersion,
  publicAccessToken: apiToken,
});

export const isShopifyConfigured = (): boolean => {
  return Boolean(storeDomain && storeDomain !== 'your-store.myshopify.com' && 
         apiToken && apiToken !== 'your_storefront_api_token');
};

